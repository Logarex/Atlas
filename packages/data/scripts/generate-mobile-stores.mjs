import { existsSync } from "node:fs";
import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join, relative, sep } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const dataRoot = join(here, "..");
const repoRoot = join(dataRoot, "..", "..");
const storesDir = join(dataRoot, "stores");
const bundlePath = join(dataRoot, "stores.json");
const mobileStoresPath = join(
  repoRoot,
  "apps",
  "mobile",
  "src",
  "features",
  "stores",
  "generatedStores.ts"
);
const mobilePhotoAssetsPath = join(
  repoRoot,
  "apps",
  "mobile",
  "src",
  "features",
  "stores",
  "generatedPhotoAssets.ts"
);
const mobileRoot = join(repoRoot, "apps", "mobile");

function toImportPath(fromFile, targetFile) {
  let value = relative(dirname(fromFile), targetFile).split(sep).join("/");
  if (!value.startsWith(".")) value = `./${value}`;
  return value;
}

function isLocalMobileAsset(url) {
  return typeof url === "string" && url.startsWith("./assets/");
}

const files = (await readdir(storesDir)).filter((file) => file.endsWith(".json")).sort();
const storesById = new Map();

for (const file of files) {
  const store = JSON.parse(await readFile(join(storesDir, file), "utf8"));
  storesById.set(store.id, store);
}

let orderedIds = [];
if (existsSync(bundlePath)) {
  const existingStores = JSON.parse(await readFile(bundlePath, "utf8"));
  orderedIds = existingStores.map((store) => store.id).filter((id) => storesById.has(id));
}

for (const id of storesById.keys()) {
  if (!orderedIds.includes(id)) orderedIds.push(id);
}

const stores = orderedIds.map((id) => storesById.get(id));
const localPhotoUrls = new Set();
const missingAssets = [];

for (const store of stores) {
  for (const photo of store.photos ?? []) {
    if (!isLocalMobileAsset(photo.url)) continue;

    localPhotoUrls.add(photo.url);
    const assetPath = join(mobileRoot, photo.url.replace(/^\.\//, ""));
    if (!existsSync(assetPath)) {
      missingAssets.push(`${store.id}: ${photo.url}`);
    }
  }
}

if (missingAssets.length > 0) {
  console.error("Missing local photo asset(s):");
  for (const asset of missingAssets) {
    console.error(`  - ${asset}`);
  }
  process.exit(1);
}

await mkdir(dirname(mobileStoresPath), { recursive: true });

await writeFile(bundlePath, `${JSON.stringify(stores, null, 2)}\n`);

const storesContent = `/* This file is generated by packages/data/scripts/generate-mobile-stores.mjs. */\nimport type { StoreRecord } from "./store.types";\n\nexport const generatedStores: StoreRecord[] = ${JSON.stringify(stores, null, 2)};\n`;
await writeFile(mobileStoresPath, storesContent);

const localPhotoEntries = [...localPhotoUrls].sort().map((url) => {
  const assetPath = join(mobileRoot, url.replace(/^\.\//, ""));
  const requirePath = toImportPath(mobilePhotoAssetsPath, assetPath);
  return `  ${JSON.stringify(url)}: require(${JSON.stringify(requirePath)})`;
});

const photosContent = `/* This file is generated by packages/data/scripts/generate-mobile-stores.mjs. */\n\nexport const localPhotosMap: Record<string, unknown> = {\n${localPhotoEntries.join(",\n")}\n};\n`;
await writeFile(mobilePhotoAssetsPath, photosContent);

console.log(`Generated ${stores.length} mobile store record(s).`);
console.log(`Mapped ${localPhotoEntries.length} local photo asset(s).`);
